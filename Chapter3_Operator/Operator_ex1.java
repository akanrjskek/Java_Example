package Chapter3_Operator;

public class Operator_ex1 {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int aplusb = a + b;
        int aminusb = a - b;
        int amulb = a * b;
        int adivb = a / b;
        int aremainb = a % b;
        System.out.println(aplusb);
        System.out.println(aminusb);
        System.out.println(amulb);
        System.out.println(adivb);
        System.out.println(aremainb);
    }
}
