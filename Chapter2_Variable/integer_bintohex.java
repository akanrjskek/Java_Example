package Chapter2_Variable;

public class integer_bintohex {
    public static void main(String[] args) {
        int v1 = 0b10000; // 2진수(0b)
        int v2 = 020; // 8진수(0)
        int v3 = 16; // 10진수()
        int v4 = 0x10; // 16진수(0x)

        System.out.println(v1 + " " + v2 + " " + v3 + " " + v4);
    }
}
