package Chapter10_Package;

import static java.lang.Math.*;

public class Math_Example2 {
    public static void main(String[] args) {
        Double d1 = 1.2;
        Double d2 = 2.3;
        Double d3 = -3.5;
        System.out.println(max(d1,d2));
        System.out.println(min(d1,d2));
        System.out.println(abs(d3));
        System.out.println(ceil(d1));
        System.out.println(floor(d1));
        System.out.println(round(d2));
    }
}
