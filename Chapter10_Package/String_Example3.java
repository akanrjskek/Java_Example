package Chapter10_Package;

public class String_Example3 {
    public static void main(String[] args) {
        String s = "hello";

        System.out.println(s.compareTo("gello"));
        System.out.println(s.compareTo("hdllo"));
        System.out.println(s.compareTo("iello"));
        System.out.println(s.compareTo("helloa"));
        System.out.println(s.compareTo("hello"));
    }
}
