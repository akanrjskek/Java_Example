package Chapter4_Control_Statement;

public class Control_ex15 {
    public static void main(String[] args) {
        for(int i = 1; i <= 5; ++i){
            System.out.println("i의 값은 " + i + "입니다.");
        }
        System.out.println();
        for(int i = 5; i > 0; --i){
            System.out.println("i의 값은 " + i + "입니다.");
        }
    }
}
